public class Git {
    public static void main(String[] args) {
        System.out.println("Que es Git");
        System.out.println("Nos permite rastrear los cambios realizados en los archivos\n");
        System.out.println("Para guardar en git usando el programna Intellij Idead los pasosson:\n");
        System.out.println("Utilizamos primero los comandos git config que hace poner nuestras credenciales");
        System.out.println("git config:puede ser usado para establecer una configuración específica de usuario, como el email, nombre de usuario y tipo de formato, etc.");
        System.out.println("git config --global user.email(email@gmail.com)");
        System.out.println("git config --global user.name(Jean)\n");
        System.out.println("git init :reará un nuevo repositorio local GIT.\n");
        System.out.println("git status:muestra la lista de los archivos que se han cambiado junto con los archivos que están por ser preparados o confirmados.\n");
        System.out.println("git add .:git add se usa para agregar archivos al área de preparación.\n");
        System.out.println("git commit:creará una instantánea de los cambios y la guardará en el directorio git.\n");
        System.out.println("git remote add origin <host-or-remoteURL>:para conectar el repositorio local a un servidor remoto\n");
        System.out.println("git push:se usa para enviar confirmaciones locales a la rama maestra del repositorio remoto.");

    }
}
